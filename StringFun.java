class StringFun 
{
	public static void main(String[] args) 
	{

		String s1="   Study Link   ";
		String s2="Study Link";
		System.out.println("s1"+s1);
		System.out.println("s2="+s2);
		System.out.println("length of s1="+s1.length());
		System.out.println("s1.equals(s2)="+s1.equals(s2));
		System.out.println("s1.equalsIgnoreCase(s2)="+s1.equalsIgnoreCase(s2));
		System.out.println("s1.compareTo(s2)="+s1.compareTo(s2));
		System.out.println("s2.substring(6)="+s2.substring(6));
		System.out.println("s1.concat(s2)="+s1.concat(s2));
		System.out.println("s1.charAt(7)="+s1.charAt(7));
		System.out.println("s1.trim()="+s1.trim());




	}
}
